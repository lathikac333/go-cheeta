package com.dto.request;

public class DriverRegistrationReq {
    private String firstName;
    private String lastName;
    private String emailAddress;
    private String address;
    private String mobileNumber;
    private String userPassword;
    private int vehicleDetailId;
    private int branchId;

    public DriverRegistrationReq() {
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmailAddress() {
        return emailAddress;
    }

    public void setEmailAddress(String emailAddress) {
        this.emailAddress = emailAddress;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }

    public int getVehicleDetailId() {
        return vehicleDetailId;
    }

    public void setVehicleDetailId(int vehicleDetailId) {
        this.vehicleDetailId = vehicleDetailId;
    }

    public int getBranchId() {
        return branchId;
    }

    public void setBranchId(int branchId) {
        this.branchId = branchId;
    }
}
